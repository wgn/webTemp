package com.zhuani21.jee.base.bean;

import java.util.Date;

public class Unno {
	private Date period;
	private float pre_value;
	private float now_value;
	private Date create_date;
	public Date getPeriod() {
		return period;
	}
	public void setPeriod(Date period) {
		this.period = period;
	}
	public float getPre_value() {
		return pre_value;
	}
	public void setPre_value(float pre_value) {
		this.pre_value = pre_value;
	}
	public float getNow_value() {
		return now_value;
	}
	public void setNow_value(float now_value) {
		this.now_value = now_value;
	}
	public Date getCreate_date() {
		return create_date;
	}
	public void setCreate_date(Date create_date) {
		this.create_date = create_date;
	}
	
	

}
